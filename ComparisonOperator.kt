fun main(args: Array<String>) {
    var a = 10
    var b = 20
     //1. > (greater than)
    println(a>b)
    //2. < (less than)
    println(a<b)
    //3. >= (less than equal)
    println(a>=b)
    //4. <= (greater than equal)
    println(a<=b)
    //5. == (less then equal)
    println(a==b)
    //6. != (not equal)
    println(a!=b)

}