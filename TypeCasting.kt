fun main(args: Array<String>) {
    var a:Int = 10
    println(a)
    //Int to double
    var b:Double = a.toDouble()
    println(b)
    println(a.toDouble())

    //Int to byte
    var num:Int = 54000
    println(num.toByte())

    //String to Int
    var str: String = "35"
    println(str.toInt())

    //var name:string = "Musfiq"
    //println(name.toInt())

    var ch:Char = 'A'
    println(ch.toInt())

    //Int to Byte
    var c:Int = 54802
    println(c.toByte())
}