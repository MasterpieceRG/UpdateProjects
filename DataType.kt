fun main(args: Array<String>) {
    var num1:Double = 5.0
    var num2:Double = 2.0
    //1. + (addition)
    var add = num1 + num2
    println("Addition: ${add}")
    //2. - (subtraction)
    var sub = num1 - num2
    println("Subtraction: ${sub}")
    //3. * (multiplication)
    var malti = num1 * num2
    println("Multiplication: ${malti}")
    //4. / (division)
    var div = num1/num2
    println("Division: ${div}")
    //5. %(modulus)
    var mod = num1 % num2
    println("Modulus: ${mod}")
}